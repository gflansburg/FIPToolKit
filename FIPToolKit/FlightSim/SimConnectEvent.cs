    public class SimConnectEvent
    {
        public SimConnectEventId Id { get; set; }
        public string Name 
        { 
            get
            {
                if (Id == SimConnectEventId.SelectAttributeToWrite)
                {
                    return "--Select Attribute To Write--";
                }
                return Id.ToString().ToTitleCase();
            }
        }
        public string Command { get; set; }
        public bool IsInitialized { get; set; }
        public DateTime LastUpdate { get; set; }
        public string Units { get; set; }
        public string Description { get; set; }
        public uint Value { get; set; }
        public bool ForceUpdate { get; set; }

        public List<NotifyChangeHandler> Delegates = new List<NotifyChangeHandler>();

        public TimeSpan MaxAge { get; private set; } = TimeSpan.FromSeconds(5);

        public delegate void NotifyChangeHandler(SimConnectEvent sender, uint newValue);
        public event NotifyChangeHandler OnValueChange;

        public SimConnectEvent()
        {
            IsInitialized = false;
            LastUpdate = DateTime.MinValue;
            Value = uint.MinValue;
        }

        public TimeSpan Age
        {
            get
            {
                return DateTime.Now - LastUpdate;
            }
        }

        protected virtual void ValueChanged()
        {
            OnValueChange?.Invoke(this, Value);
        }

        public bool Update(uint value)
        {
            LastUpdate = DateTime.Now;
            if (value != Value || ForceUpdate || !IsInitialized)
            {
                ForceUpdate = false;
                IsInitialized = true;
                Value = value;
                ValueChanged();
                return true;
            }
            return false;
        }
    }
