    public class DataRefElement
    {
        private static int current_id = 0;
        private static object lockElement = new object();

        public DataRefId Id { get; set; }
        public string Name { get; set; }
        public string DataRef { get; set; }
        public int Frequency { get; set; }
        public bool IsInitialized { get; set; }
        public bool IsSubscribed { get; set; }
        public DateTime LastUpdate { get; set; }
        public string Units { get; set; }
        public string Description { get; set; }
        public float Value { get; set; }
        public bool Subscribed { get; set; }
        public bool ForceUpdate { get; set; }
        public int DataGramId { get; private set; }
        public int Index { get; set; }
        public bool Writable { get; set; }
        public string DataType { get; set; }
        public List<NotifyChangeHandler> Delegates = new List<NotifyChangeHandler>();

        public TimeSpan MaxAge { get; private set; } = TimeSpan.FromSeconds(5);

        public delegate void NotifyChangeHandler(DataRefElement sender, float newValue);
        public event NotifyChangeHandler OnValueChange;

        public DataRefElement()
        {
            lock(lockElement)
            {
                DataGramId = ++current_id;
            }
            IsInitialized = false;
            LastUpdate = DateTime.MinValue;
            Value = float.MinValue;
        }


        public TimeSpan Age
        {
            get
            {
                return DateTime.Now - LastUpdate;
            }
        }

        protected virtual void ValueChanged()
        {
            OnValueChange?.Invoke(this, Value);
        }

        public bool Update(int id, float value)
        {
            if (id == DataGramId)
            {
                LastUpdate = DateTime.Now;

                if (value != Value || ForceUpdate)
                {
                    ForceUpdate = false;
                    Value = value;
                    IsInitialized = true;
                    ValueChanged();
                    return true;
                }
            }

            return false;
        }
    }
